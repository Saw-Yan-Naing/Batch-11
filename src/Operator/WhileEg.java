package Operator;

public class WhileEg {
    public static void main(String[] args) {
        int a = 3;
        while (a < 5) {
            System.out.println(a);
            a++;
        }
        int b=8;
        do{
            System.out.println(b);
        }while(b<7);

        int j=4;
        do{
            System.out.println(j);
           j++;
        }while(j<6);
    }
}
