package Practise;

public class ForLoop {
    public static void main(String[] args) {

        //for loop

        for (int i=3;i<=10;i++){
            System.out.print(i+"\t");
        }
        System.out.println();

        //for i loop

        for (int i1 = 0; i1 <5 ; i1++) {
            System.out.print(i1+"\t");
        }

        //for each loop



    }
}
